int	ft_sqrt(int nb)
{
	int	rt;

	if (nb < 0)
		return (0);
	rt = 1;
	while (rt * rt <= nb && rt * rt > 0)
	{
		if (rt * rt == nb)
			return (rt);
		rt++;
	}
	return (0);
}
