int	ft_count_if(char **tab, int (*f)(char *))
{
	int	cnt;
	int	i;

	cnt = 0;
	i = -1;
	while (tab[++i])
	{
		if (f(tab[i]))
			cnt++;
	}
	return (cnt);
}
